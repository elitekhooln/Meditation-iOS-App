- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions {
    // Override point for customization after application launch.
    
    NSUserDefaults *defaults = [NSUserDefaults standardUserDefaults];
    NSDate *totalMedyTime = [defaults objectForKey:@"medyTime"];
    
    if (!totalMedyTime) {
        NSDateComponents *totalMedycomponents = [[NSCalendar currentCalendar] components:NSHourCalendarUnit|NSMinuteCalendarUnit|NSSecondCalendarUnit
                                                                                fromDate:[NSDate date]
                                                                                  toDate:[NSDate date]
                                                                                 options:0];
        
        totalMedyTime = [[NSCalendar currentCalendar] dateFromComponents:totalMedycomponents];
        [defaults setObject:totalMedyTime forKey:@"medyTime"];
        [defaults synchronize];
    }
    
    NSNumber *userLevel = [defaults objectForKey:@"userLevel"];
    if (!userLevel) {
        userLevel = [NSNumber numberWithInt:0];
        [defaults setObject:userLevel forKey:@"userLevel"];
        [defaults synchronize];
    }
    
    // Handle launching from a notification
    UILocalNotification *locationNotification = [launchOptions objectForKey:UIApplicationLaunchOptionsLocalNotificationKey];
    if (locationNotification) {
        // Set icon badge number to zero
        application.applicationIconBadgeNumber = 0;
    }
    
    [self registerForRemoteNotification];
    
    return YES;
}

- (void)registerForRemoteNotification {
    if (SYSTEM_VERSION_GREATER_THAN_OR_EQUAL_TO(@"8.0")) {
        UIUserNotificationType types = UIUserNotificationTypeSound | UIUserNotificationTypeBadge | UIUserNotificationTypeAlert;
        UIUserNotificationSettings *notificationSettings = [UIUserNotificationSettings settingsForTypes:types categories:nil];
        [[UIApplication sharedApplication] registerUserNotificationSettings:notificationSettings];
    } else {
        [[UIApplication sharedApplication] registerForRemoteNotificationTypes:(UIRemoteNotificationTypeBadge | UIRemoteNotificationTypeSound | UIRemoteNotificationTypeAlert)];
    }
}

#ifdef __IPHONE_8_0
- (void)application:(UIApplication *)application didRegisterUserNotificationSettings:(UIUserNotificationSettings *)notificationSettings {
    [application registerForRemoteNotifications];
}
#endif

